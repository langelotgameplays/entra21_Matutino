package exemplo02;

public class Pessoa {

	//Atributos
	private String nome;
	private int indade;
	
	//Métodos mágicos
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIndade() {
		return indade;
	}
	public void setIndade(int indade) {
		this.indade = indade;
	}
	
	
}
