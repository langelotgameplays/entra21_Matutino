package beans;

public class Beans {
	
	//Atributos
	private String colaborador, sBruto, vTransporte, fMes, cargo, cFidelidade, pSaude, iRenda, sLiquido;

	//Get & Set
	public String getColaborador() {
		return colaborador;
	}

	public void setColaborador(String colaborador) {
		this.colaborador = colaborador;
	}

	public String getsBruto() {
		return sBruto;
	}

	public void setsBruto(String sBruto) {
		this.sBruto = sBruto;
	}

	public String getvTransporte() {
		return vTransporte;
	}

	public void setvTransporte(String vTransporte) {
		this.vTransporte = vTransporte;
	}

	public String getfMes() {
		return fMes;
	}

	public void setfMes(String fMes) {
		this.fMes = fMes;
	}

	public String getCargo() {
		return cargo;
	}

	public void setCargo(String cargo) {
		this.cargo = cargo;
	}

	public String getcFidelidade() {
		return cFidelidade;
	}

	public void setcFidelidade(String cFidelidade) {
		this.cFidelidade = cFidelidade;
	}

	public String getpSaude() {
		return pSaude;
	}

	public void setpSaude(String pSaude) {
		this.pSaude = pSaude;
	}

	public String getiRenda() {
		return iRenda;
	}

	public void setiRenda(String iRenda) {
		this.iRenda = iRenda;
	}

	public String getsLiquido() {
		return sLiquido;
	}

	public void setsLiquido(String sLiquido) {
		this.sLiquido = sLiquido;
	}
	

}
